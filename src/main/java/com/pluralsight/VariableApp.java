package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {

        String favoriteColor = "Blue";
        System.out.println("The answer is" + (favoriteColor));
        int yearStarted = 2025;
        System.out.println("The answer is" + (yearStarted));
        char middleInitial = 'V';
        System.out.println("The answer is" + (middleInitial));
        String pets = "Yes";
        System.out.println("Do you have a pet?" + (pets));
        String niceMessage = "You're nice.";
        System.out.println("You're message is that" + (niceMessage));


    }

}